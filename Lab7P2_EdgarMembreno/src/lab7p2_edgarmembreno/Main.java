    private void AgregarTripulanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarTripulanteActionPerformed
        adminAstro ap = new adminAstro("./Astronautas.cbm");
        ap.cargarArchivo();
        for (int i = 0; i < ap.getListaAstro().size(); i++) {
            int id = ap.getListaAstro().get(i).getId();
            String nombre = ap.getListaAstro().get(i).getNombre();
            String apellido = ap.getListaAstro().get(i).getApellido();
            String nacionalidad = ap.getListaAstro().get(i).getNacionalidad();
            String titulo = ap.getListaAstro().get(i).getTitulo();
            String contex = ap.getListaAstro().get(i).getContextura();
            String peso = ap.getListaAstro().get(i).getPeso();
            String misones = ap.getListaAstro().get(i).getMisones();

            astronautas x = new astronautas(id, nombre, apellido, nacionalidad, titulo, contex, peso, misones);

            adminNaves ad = new adminNaves("./Naves.cbm");
            ad.cargarArchivo();
            ad.getListaNaves().get(i).getAstronautas().add(x);
            ap.escribirArchivo();

        }
        JOptionPane.showMessageDialog(this, "Se agrego tripulante");


    }//GEN-LAST:event_AgregarTripulanteActionPerformed

