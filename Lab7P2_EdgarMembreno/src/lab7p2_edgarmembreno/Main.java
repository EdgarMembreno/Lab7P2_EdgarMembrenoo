    private void EmpezarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpezarViajeActionPerformed
        try {
            adminNaves ad = new adminNaves("./Naves.cbm");
            ad.cargarArchivo();
            if (ad.getListaNaves().get(poNave).getAstronautas().get(poNave).getTitulo().equalsIgnoreCase("Fisico")
                    && ad.getListaNaves().get(poNave).getAstronautas().get(poNave).getTitulo().equalsIgnoreCase("Astrofisico")
                    && ad.getListaNaves().get(poNave).getAstronautas().get(poNave).getTitulo().equalsIgnoreCase("Ingeniero")&& ad.getListaNaves().get(poNave).getPersonasMax()>=3) {
                via = new Viaje(progress_Distancia);
                via.setPo(jl_Destino.getSelectedIndex());
                naves x = ad.getListaNaves().get(poNave);
                via.setNave(x);
                try {
                    via.start();
                } catch (Exception e) {
                }
                via.setAvanzar(true);
                jd_Viaje.setModal(true);
                jd_Viaje.pack();
                jd_Viaje.setLocationRelativeTo(this);
                jd_Viaje.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this, "Debe de tener un fisico, astrofisico y un ingeniero para lograr la mision");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "OCURRIO UN ERROR");
        }


    }//GEN-LAST:event_EmpezarViajeActionPerformed

