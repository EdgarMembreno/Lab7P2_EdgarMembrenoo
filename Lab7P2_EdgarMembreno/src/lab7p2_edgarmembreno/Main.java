    private void agregarAstroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarAstroActionPerformed
        try {
            int id = Integer.parseInt(idAstro.getText());
            String nombre = nombreAstro.getText();
            String apellido = apellidoAstro.getText();
            String nacionalidad = nacionalidadAstro.getText();
            String titulo = tituloAstro.getText();
            String contextura = contexturaAstro.getText();
            String peso = pesoAstro.getText();
            String misiones = misionesAstro.getText();

            astronautas x = new astronautas(id, nombre, apellido, nacionalidad, titulo, contextura, peso, misiones);
            adminAstro ap = new adminAstro("./Astronautas.cbm");
            ap.cargarArchivo();
            ap.setAstro(x);
            ap.escribirArchivo();

            idAstro.setText("");
            nombreAstro.setText("");
            apellidoAstro.setText("");
            nacionalidadAstro.setText("");
            tituloAstro.setText("");
            contexturaAstro.setText("");
            pesoAstro.setText("");
            misionesAstro.setText("");
            jd_CrearAstro.hide();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "OCURRIO UN ERROR");
        }


    }//GEN-LAST:event_agregarAstroActionPerformed

